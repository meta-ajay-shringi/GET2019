package dsAssignment1;

/**
 * This class is used to create a node of linkedlist.
 */
public class Node {
	
	int data;
	Node next;
	
	/**
	 * Constructor to initialize the data members of the class.
	 * @param data, put data in linkedlist node.
	 */
	Node(int data) {
		this.data = data;
		this.next = null;
	}

}
