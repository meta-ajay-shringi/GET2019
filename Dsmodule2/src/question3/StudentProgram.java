/**
 * This class is used to store student allocate programs. 
 */
public class StudentProgram {

	String name;
	String program;
	
	/**
	 * This is constructor of student program.
	 * @param name
	 * @param program
	 */
	StudentProgram(String name, String program) {
		this.name = name;
		this.program = program;
		
	}
}
